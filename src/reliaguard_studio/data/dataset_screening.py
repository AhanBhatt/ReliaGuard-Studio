def write_dataset_screening() -> dict[str, Path]:
    REAL_DATA_EXPERIMENTS_DIR.mkdir(parents=True, exist_ok=True)
    submission_dir = PAPER_DIR / "nmi_submission"
    submission_dir.mkdir(parents=True, exist_ok=True)
    df = pd.DataFrame(SCREENED_DATASETS)
    csv_path = REAL_DATA_EXPERIMENTS_DIR / "dataset_screening.csv"
    tex_path = submission_dir / "supplementary_dataset_screening.tex"
    df.to_csv(csv_path, index=False)
    tex_columns = ["name", "availability", "initial_judgment", "final_judgment", "ground_truth", "decision", "reason"]
    tex = df[tex_columns].to_latex(index=False, escape=True, longtable=True)
    tex_path.write_text(tex, encoding="utf-8")
    return {"dataset_screening": csv_path, "supplementary_dataset_screening": tex_path}


if __name__ == "__main__":
    write_dataset_screening()
